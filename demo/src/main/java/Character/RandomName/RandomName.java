    public static String getRandomName() {
        Random random = new Random();
        String surname;
        double rand = random.nextDouble();
        if (rand<0.1) {
            return specialName[random.nextInt(specialName.length)];
        }
        // 按比例选择姓氏类型（10%英文译姓/15%复姓/75%单姓）
        surname=surnames[random.nextInt(surnames.length)];
        // 生成名字（70%双字名/30%单字名）
        rand=random.nextDouble();
        if(rand<0.3){return surname+singleNameChars[random.nextInt(singleNameChars.length)];}
        String givenName;
        givenName=nameChar_first[random.nextInt(nameChar_first.length)];
        givenName+=nameChar_second[random.nextInt(nameChar_second.length)];
        return surname+givenName;
    }
